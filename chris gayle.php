    with 10408 runs to his name.
<br>2. Chris Gayle holds the record of smashing the first century in Twenty20 Internationals. He scored a blistering 57-ball 
117 against South Africa in the opening match of the first edition of World Twenty20 in 2007. 

<br>3. In 2015, Gayle became the first man to hit a double-hundred in a World Cup match. The occasion came when he hammered 
215 against Zimbabwe.
<br>4. The West Indian is one of only four batsmen to have scored two Test triple hundreds. The other three batsmen in this elite company 
being Don Bradman, Brian Lara, and Virender Sehwag. 
