<br> 6.He is the first player to hit all 6 balls in an over for four in Test cricket. He achieved the feat against former England pacer Matthew 
Hoggard in a Test match in 2004. 
<br> 7.The Jamaican is the only batsman to have a triple hundred in Tests, double hundred in ODIs and a hundred in T20Is.
<br> 8. He holds the records of the fastest century of IPL, the highest score of IPL and maximum sixes in an innings of the league. Interestingly, all the 
