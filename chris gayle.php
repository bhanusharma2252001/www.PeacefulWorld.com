<h6 class="bio-header">Who Is Chris gayle?</h6>
<p class="bio-para">Christopher Henry Gayle, OD (born 21 September 1979) is a Jamaican cricketer who plays international cricket for 
the West Indies. Gayle captained the West Indies Test side from 2007 to 2010. Considered as one of the greatest batsmen ever in 
Twenty20 (T20) cricket, Gayle has set numerous records across all three formats of the game. He is the most capped player for the West
 Indies in international cricket and is the only player to score a triplet of centuries – a triple hundred in Tests, double hundred in 
 ODIs and a hundred in T20Is.



 </p>
  <h6 class="bio-header">Early career </h6>
    <p class="bio-para">Gayle started his cricket career with Lucas Cricket Club in Kingston, Jamaica. Gayle claimed: "If it was not 
    for Lucas I don't know where I would be today. Maybe on the streets" Lucas Cricket Club's nursery has been named in honour 
    of Gayle. He was made captain of the West Indies side in the ICC T20 World Cup 2009 after getting criticized for playing in the
     IPL 2008 and missing an international series a few months before. Gayle scored a quickfire 88 in the opening game of the
      tournament and helped the Windies beat the mighty Australians. They lost in the semi-final to Sri Lanka.
      </p>
 
