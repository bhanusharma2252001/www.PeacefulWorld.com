<h6 class="bio-header">Who Is Jack Dorsey?</h6>
<p class="bio-para"> 
Jack Dorsey is an American businessman best known as the founder of the social networking site Twitter.
Jack Dorsey became involved in web development as a college student, founding the Twitter social networking site in 2006. 
Since that time, Dorsey has served as CEO, chairman of the board, and executive chairman of Twitter. He also launched the 
successful online payment platform Square in 2010.

</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Dorsey was born in St. Louis, Missouri, on November 19, 1976. Growing up in St. Louis, Dorsey became interested in computers 
    and communications at an early age and began programming while still a student at Bishop DuBourg High School. He was fascinated
     by the technological challenge of coordinating taxi drivers, delivery vans and other fleets of vehicles that needed to remain 
     in constant, real-time communication with one another. When he was 15, Dorsey wrote dispatch software that remained in use by 
     taxicab companies for decades afterward.


</p>
 
  <h6 class="bio-header">Creation of Twitter</h6>
  <p class="bio-para"> 
  After a brief stint at the Missouri University of Science and Technology, Dorsey transferred to New York University. 
  In the tradition of computer science entrepreneurs such as Bill Gates, Steve Jobs and Mark Zuckerberg, he dropped out of 
  college before receiving his degree. Instead, Dorsey moved to Oakland, California, and in 2000 he started a company offering
   his dispatch software through the internet. Shortly after starting his company, Dorsey came up with the idea for a site that 
   would combine the broad reach of dispatch software with the ease of instant messaging.
</p>
<h6 class="bio-header">Twitter Success </h6>
  <p class="bio-para"> 
  Twitter was initially derided by some as a tool for the shallow and self-centered to broadcast the minutiae of their lives to the 
  universe. Late-night comedy host Conan O'Brien even featured a segment called "Twitter Tracker" that mocked users of the service.
   In its early days, the site also suffered from frequent service outages. But as celebrities and CEOs alike began tweeting, 
   Twitter was no longer the brunt of so many jokes. Suddenly the head of the "microblogging" movement, Twitter became a powerful
    platform for U.S. Presidential candidates Barack Obama and John McCain in 2008, as a method for updating their supporters while
     on the campaign trail.
Twitter vaulted to international prominence after the June 2009 presidential elections in Iran, when thousands of opposition 
supporters took to the streets to protest the claimed victory of incumbent Mahmoud Ahmadinejad. When the government blocked text
 messaging and satellite feeds of foreign news coverage, Iranian Twitter users flooded the site with live updates. A U.S. State
  Department official even emailed Dorsey to request that Twitter delay its scheduled maintenance so that protestors could keep
   tweeting. "It appears Twitter is playing an important role at a crucial time in Iran. Could you keep it going?" said a State 
   Department spokesman, describing the call. Twitter complied.

</p>


