<h6 class="bio-header">Who Is François Hollande?</h6>
<p class="bio-para">  François Hollande was born on 12 August 1954 in Rouen. His mother, Nicole Frédérique Marguerite Tribert
 (1927–2009), was a social worker, and his father, Georges Gustave Hollande (1923–2020), is a retired ear, nose, and throat doctor 
 who "ran for local election on a far right ticket in 1959."The name "Hollande" meant "one originally from Holland" – it is mostly 
 found in Hollande's ancestral land, Hauts-de-France, and it is speculated to be Dutch in origin. The earliest known member of the
  Hollande family lived circa 1569 near Plouvain, working as a miller.
When Hollande was thirteen, the family moved to Neuilly-sur-Seine, a highly exclusive suburb of Paris. He attended Saint-Jean-Baptiste-
de-la-Salle boarding school, a private Catholic school in Rouen, the Lycée Pasteur, in Neuilly-sur-Seine, receiving his baccalaureate
 in 1972 then graduated with a bachelor's degree in Law from Panthéon-Assas University. Hollande studied at HEC Paris, graduated in
  1975, and then attended the Institut d'études politiques de Paris and the École nationale d'administration (ENA). He did his military 
  service in the French Army in 1977. He graduated from the ENA in 1980 and chose to enter the prestigious Cour des comptes.

Hollande lived in the United States in the summer of 1974 as a university student.Immediately after graduation, he was employed as a 
councillor in the Court of Audit.



</p>
  <h6 class="bio-header">Early Life and Political Career</h6>
    <p class="bio-para">The son of a physician in France’s northwestern Haute-Normandie région, Hollande 
    was educated at the elite École Nationale d’Administration, where his classmates included future prime minister
     Dominique de Villepin and future Socialist presidential candidate Ségolène Royal, who would also become Hollande’s 
     longtime companion. In 1979, while still a student, Hollande joined the Socialist Party, and he worked as an economic 
     adviser in the administration of Pres. François Mitterrand. He graduated from the école in 1980. The following year Hollande
      unsuccessfully challenged Jacques Chirac for the parliamentary seat representing Corrèze département but was appointed special
       assistant on economic affairs in the Mitterrand administration. Hollande next served as chief of staff for two successive 
       government spokespersons for Prime Minister Pierre Mauroy.

In 1988 Hollande was elected to represent Corrèze in the National Assembly. He lost the seat in 1993 but won it back in 1997.
 That same year he succeeded Lionel Jospin as leader of the Socialist Party, after Jospin was appointed prime minister in a 
 cohabitation government in which the conservative Chirac served as president. In addition to his party chairmanship and his 
 seat in the National Assembly, Hollande held a number of local and provincial posts during this time, most notably serving as
  mayor of Tulle (2001–08). As party leader, Hollande was sometimes overshadowed by fellow Socialists who were perhaps more 
  outwardly telegenic, such as Royal or the economist Dominique Strauss-Kahn. In time Hollande came to embrace this status, 
  and he willingly accepted the nickname “Monsieur Normal” that was bestowed upon him by the press. After Socialist candidates
   lost two successive presidential elections—Jospin was trounced in the 2002 election’s first round of voting, and Royal lost to
    Nicolas Sarkozy in 2007—Hollande resigned as party leader in 2008.
</p>
 
  <h6 class="bio-header">Early political career</h6>
  <p class="bio-para"> 
  Five years after volunteering as a student to work for François Mitterrand's ultimately unsuccessful campaign in the 1974 
  presidential election, Hollande joined the Socialist Party. He was quickly spotted by Jacques Attali, a senior adviser to Mitterrand,
   who arranged for Hollande to run in legislative election of 1981 in Corrèze against future President Jacques Chirac, who was then 
   the leader of the Rally for the Republic, a Neo-Gaullist party. Hollande lost to Chirac in the first round.
He went on to become a special advisor to newly elected President Mitterrand, before serving as a staffer for Max Gallo, the 
government's spokesman. After becoming a municipal councillor for Ussel in 1983, he contested Corrèze for a second time in 1988, 
this time being elected to the National Assembly. Hollande lost his bid for re-election to the Assembly in the so-called "blue wave"
 of the 1993 election, described as such due to the number of seats gained by the Right at the expense of the Socialist Party.





</p>

   


