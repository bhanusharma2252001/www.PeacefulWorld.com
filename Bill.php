<h6 class="bio-header">Who is Bill Gates?</h6>
<p class="bio-para">  Entrepreneur Bill Gates founded the world's largest software business, Microsoft, with Paul Allen, and 
subsequently became one of the richest men in the world.
Who Is Bill Gates?
Entrepreneur and businessman Bill Gates and his business partner Paul Allen founded and built the world's largest software business,
 Microsoft, through technological innovation, keen business strategy and aggressive business tactics. In the process, Gates became 
 one of the richest men in the world. In February 2014, Gates announced that he was stepping down as Microsoft's chairman to focus 
 on charitable work at his foundation, the Bill and Melinda Gates Foundation.






</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para"> Gates was born William Henry Gates III on October 28, 1955, in Seattle, Washington. Gates grew up in an 
    upper-middle-class family with his older sister, Kristianne, and younger sister, Libby. Their father, William H. Gates Sr.,
     was a promising, if somewhat shy, law student when he met his future wife, Mary Maxwell. She was an athletic, outgoing student at the University of Washington, actively involved in student affairs and leadership.

The Gates family atmosphere was warm and close, and all three children were encouraged to be competitive and strive for excellence. 
Gates showed early signs of competitiveness when he coordinated family athletic games at their summer house on Puget Sound.
 He also relished in playing board games (Risk was his favorite) and excelled at Monopoly.

Gates had a very close relationship with his mother, Mary, who after a brief career as a teacher devoted her time to helping 
raise the children and working on civic affairs and with charities. She also served on several corporate boards, including those
 of the First Interstate Bank in Seattle (founded by her grandfather), the United Way and International Business Machines (IBM).
  She would often take Gates along when she volunteered in schools and at community organizations.







</p>
 
  <h6 class="bio-header">Education</h6>
  <p class="bio-para"> 
  Gates was a voracious reader as a child, spending many hours poring over reference books such as the encyclopedia. 
  Around the age of 11 or 12, Gates's parents began to have concerns about his behavior. He was doing well in school, but he
   seemed bored and withdrawn at times, and his parents worried he might become a loner.

Though they were strong believers in public education, when Gates turned 13, his parents enrolled him at Seattle's exclusive
 preparatory Lakeside School. He blossomed in nearly all his subjects, excelling in math and science, but also doing very well
  in drama and English.

While at Lakeside School, a Seattle computer company offered to provide computer time for the students. The Mother's Club used 
proceeds from the school's rummage sale to purchase a teletype terminal for students to use. Gates became entranced with what a 
computer could do and spent much of his free time working on the terminal. He wrote a tic-tac-toe program in BASIC computer language 
that allowed users to play against the computer.

Gates graduated from Lakeside in 1973. He scored 1590 out of 1600 on the college SAT test, a feat of intellectual achievement
 that he boasted about for several years when introducing himself to new people.






</p>
<h6 class="bio-header">Meeting and Partnering with Paul Allen</h6>
   <p class="bio-para"> Gates met Allen, who was two years his senior, in high school at Lakeside School. 
   The pair became fast friends, bonding over their common enthusiasm for computers, even though they were very different people.
    Allen was more reserved and shy. Gates was feisty and at times combative.
<br>
Regardless of their differences, Allen and Gates spent much of their free time together working on programs. 
Occasionally, the two disagreed and would clash over who was right or who should run the computer lab. On one occasion,
 their argument escalated to the point where Allen banned Gates from the computer lab.
 <br>
At one point, Gates and Allen had their school computer privileges revoked for taking advantage of software glitches to
 obtain free computer time from the company that provided the computers. After their probation, they were allowed back in 
 the computer lab when they offered to debug the program. During this time, Gates developed a payroll program for the computer
  company the boys had hacked into and a scheduling program for the school.
  <br>
In 1970, at the age of 15, Gates and Allen went into business together, developing "Traf-o-Data," a computer program 
that monitored traffic patterns in Seattle. They netted $20,000 for their efforts. Gates and Allen wanted to start their
 own company, but Gates' parents wanted him to finish school and go on to college, where they hoped he would work to become a lawyer.
 <br>
Allen went to Washington State University, while Gates went to Harvard, though the pair stayed in touch. After attending 
college for two years, Allen dropped out and moved to Boston, Massachusetts, to work for Honeywell. Around this time, 
he showed Gates an edition of Popular Electronics magazine featuring an article on the Altair 8800 mini-computer kit. 
Both young men were fascinated with the possibilities of what this computer could create in the world of personal computing.
<br>
The Altair was made by a small company in Albuquerque, New Mexico, called Micro Instrumentation and Telemetry Systems (MITS).
 Gates and Allen contacted the company, proclaiming that they were working on a BASIC software program that would run the Altair
  computer. In reality, they didn't have an Altair to work with or the code to run it, but they wanted to know if MITS was 
  interested in someone developing such software.
  <br>
MITS was, and its president, Ed Roberts, asked the boys for a demonstration. Gates and Allen scrambled, spending the next 
two months writing the BASIC software at Harvard's computer lab. Allen traveled to Albuquerque for a test run at MITS, never
 having tried it out on an Altair computer. It worked perfectly. Allen was hired at MITS, and Gates soon left Harvard to work 
 with him. Together they founded Microsoft.
 <br>
Allen remained with Microsoft until 1983, when he was diagnosed with Hodgkin's disease. Though his cancer went into remission
 a year later with intensive treatment, Allen resigned from the company. Rumors abound as to why Allen left Microsoft. 
 Some say Gates pushed him out, but many say it was a life-changing experience for Allen and he saw there were other 
 opportunities that he could invest his time in.


</p>


<h6 class="bio-header">Bill Gates and Founding Microsoft</h6>
In 1975, Gates and Allen formed Micro-Soft, a blend of "micro-computer" and "software" (they dropped the hyphen within a year). 
The company's first product was BASIC software that ran on the Altair computer.
At first, all was not smooth sailing. Although Microsoft’s BASIC software program for the Altair computer netted the company a 
fee and royalties, it wasn't meeting their overhead. According to Gates' later account, only about 10 percent of the people 
using BASIC in the Altair computer had actually paid for it.
<br>
Microsoft's BASIC software was popular with computer hobbyists, who obtained pre-market copies and were reproducing and 
distributing them for free. At this time, many personal computer enthusiasts were not in it for the money. They felt the
 ease of reproduction and distribution allowed them to share software with friends and fellow computer enthusiasts. Gates 
 thought differently. He saw the free distribution of software as stealing, especially when it involved software that was 
 created to be sold.
 <br>
In February 1976, Gates wrote an open letter to computer hobbyists, saying that continued distribution and use of softwar
e without paying for it would "prevent good software from being written." In essence, pirating software would discourage
 developers from investing time and money into creating quality software. The letter was unpopular with computer enthusiasts, 
 but Gates stuck to his beliefs and would use the threat of innovation as a defense when faced with charges of unfair business
  practices.
  <br>
Gates had an acrimonious relationship with MITS president Ed Roberts, often resulting in shouting matches. The combative Gates 
clashed with Roberts on software development and the direction of the business. Roberts considered Gates spoiled and obnoxious. 

In 1977, Roberts sold MITS to another computer company and went back to Georgia to enter medical school and become a doctor.
<br>
Gates and Allen were on their own. The pair had to sue the new owner of MITS to retain the software rights they had developed for
 Altair. Microsoft wrote software in different formats for other computer companies, and, at the beginning of 1979, Gates moved the
  company's operations to Bellevue, Washington, just east of Seattle.
  <br>
Gates was glad to be home again in the Pacific Northwest and threw himself into his work. All 25 employees of the young company 
had broad responsibilities for all aspects of the operation, product development, business development and marketing.
<br>
Although the company started out on shaky footing, by 1979 Microsoft was grossing approximately $2.5 million. At the age of 23,
 Gates placed himself as the head of the company. With his acumen for software development and a keen business sense, he led the
  company and worked as its spokesperson. Gates personally reviewed every line of code the company shipped, often rewriting code
   himself when he saw it necessary.





