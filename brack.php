  <h6 class="bio-header">First Book and Grammy</h6>
  <p class="bio-para">
Obama published his autobiography, Dreams from My Father: A Story of Race and Inheritance, in 1995. The work received high praise
 from literary figures such as Toni Morrison. It has since been printed in more than 25 languages, including Chinese, Swedish and
  Hebrew. The book had a second printing in 2004 and was adapted for a children's version.

The audiobook version of Dreams, narrated by Obama, received a Grammy Award for Best Spoken Word album in 2006.
