Who Is Barack Obama?
Barack Obama was the 44th president of the United States and the first African American commander-in-chief. He served two terms, in 
2008 and 2012. The son of parents from Kenya and Kansas, Obama was born and raised in Hawaii. He graduated from Columbia University 
and Harvard Law School, where he was president of the Harvard Law Review. After serving on the Illinois State Senate, he was elected
 a U.S. senator representing Illinois in 2004. He and wife Michelle Obama have two daughters, Malia and Sasha. 


