<h6 class="bio-header">Who Is Alice Louise Walton?</h6>
<p class="bio-para"> 
Alice Louise Walton (born October 7, 1949) is an American heiress to the fortune of Walmart Inc. She is the only daughter of Walmart 
founder Sam Walton and Helen Walton, and sister of S. Robson Walton, Jim Walton and the late John T. Walton.
In September 2016, she owned over US$11 billion in Walmart shares. As of May 2020, she was ranked as the 9th-richest person 
in the world and the richest woman in the world, with an estimated net worth of $56 billion. And as of July 2020, according to Forbes,
 Alice Walton was ranked 11th in Forbes 400 at an estimated net worth of $62.9 billion.
</p>
  <h6 class="bio-header">Early life Education</h6>
    <p class="bio-para"> 
    Walton was born in Newport, Arkansas. She was raised along with her 3 brothers in Bentonville, Arkansas and graduated from 
    Bentonville HS in 1966. She graduated from Trinity University in San Antonio, Texas, with a B.A. in economics 
    
   </p>
     
   <h6 class="bio-header">Career</h6>
    <p class="bio-para"> 
    In her early career, Walton was an equity analyst and money manager for First Commerce Corporation and headed investment activities
     at Arvest Bank Group. She was also a broker for EF Hutton. In 1988, Walton founded Llama Company, an investment bank, where she 
     was president, chairwoman and CEO.
Walton was the first person to chair the Northwest Arkansas Council and played a major role in the development of the Northwest
 Arkansas Regional Airport, which opened in 1998. At the time, the business and civic leaders of Northwest Arkansas Council found a 
 need for the $109 million regional airport in their corner of the state. Walton provided $15 million in initial funding for 
 construction. Her company, Llama Company, underwrote a $79.5 million bond. The Northwest Arkansas Regional Airport Authority 
 recognized Walton's contributions to the creation of the airport and named the terminal the Alice L. Walton Terminal Building.
  She was inducted into the Arkansas Aviation Hall of Fame in 2001.
</p>
