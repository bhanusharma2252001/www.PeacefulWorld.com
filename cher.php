<h6 class="bio-header">Who Is Cher Wang?</h6>
<p class="bio-para"> 
The technology has remained one of the few fields where women are not expected to make a mark. 
Men have dominated this field since the beginning with an estimated seventy percent of significant jobs.
But, one can make the mark only when he/she struggles with the most. Likewise, there are some women who have broken all 
stereotypes and established their names in the world of technology, despite the fact that this field is dominated by tech savvy men. 
One such woman is Cher Wang of HTC. Being the founder of HTC, Cher has reached the zenith of success in the world of technology.
</p>
  <h6 class="bio-header">Life before HTC</h6>
    <p class="bio-para">   
    Born in one of the powerful business family on 14 September 1968, Cher Wang is the daughter of Wang Yung-ching. 
    By the name of Farmosa Plastics Group, her father founded the petrochemicals and plastics conglomerate. Born to become
     an entrepreneur, Cher was sent to live in Los Angeles with her sister when she turned fifteen.
However, she could not spend much time in Los Angeles as her sister’s husband got a new job, Charlene relocated to Detroit. 
But her sister didn’t drag her to Detroit, instead Charlene enrolled her in one of the best schools in Oakland, The College 
Preparatory School.
After finishing high school, she moved on to get higher education. However, being a lover of piano, she wanted to pursue a career 
in music at Berkeley. But, some people are destined not to follow their initial instincts, she gave up her love for piano to study 
economics. Cher joined her brother’s law company, First International Computer after she graduated from the University of California.
Cher travelled around Europe peddling motherboards for the newly established company, during her tenure at “First International 
Computer.” However, later, Cher launched a new personal computer brand, Leo Computers focused more on helping her grow the business
 venture. Unfortunately, it got crashed soon after the launch. But the failure could not crush Cher’s spirit.
   </p>
    <h6 class="bio-header"> Love for Branded Smart Phones</h6>
     
<p class="bio-para">
During her long trips to the Europe, Cher realized that PCs were bulky and uneasy to carry, And, thus a new handheld device was 
needed that could be easily carried in a pocket or a briefcase. Cher shared this idea with one of her close friends, H.T.Cho.
Cher was approached by an acquaintance at Microsoft, who was searching for a manufacturer to build handheld devices using Windows 
CE operating system. And that was it; she saw an excellent opportunity and thus, founded her own company, HTC in partnership with Cho.
At the advent, the technology did not exist to create handheld computers that could fit into pockets and thus, HTC decided to start
 by building the laptops. However, unluckily, the laptops were not as successful because they were lacking a competitive advantage.
  Nonetheless, Cher had a contingency plan ready to follow in case the laptops failed in the market.
<br><br>
The plan was to build mobile phones and PDAs in partnership with other companies, by becoming a contract manufacturer. 
Finally, after a lot of struggle, HTC caught its lucky break when the first contract was signed in 2000 to produce the Compaq 
Aero PDA. Following this deal, HTC had become the official contract manufacturer of mobile phones and PDAs for O2, T-Mobile, Orange,
 and Palm. HTC had also manufactured Google’s first Android phone, the G1.
In the following years, HTC had successfully built a reputation of being the official contract manufacturer of handheld devices 
for all leading mobile phone brands. But Cher was not going to stop there, she had a different vision for HTC. She wanted HTC to
 become a leading brand of smart phone and not just a contract manufacturer. She knew this decision would affect her company in the
  first few years but she didn’t stop.
  <br><br>
In the year 2006, their stock price was decreased to half of the original value for almost four long years but she didn’t lose hope. 
HTC was struggling during those years to invest more capital for building and designing smart phones under their brand name. 
But, just when Cher had started to think of the company’s expertise and engineering talent, 2007 came with a lot of surprises.
 The company had forged a bond of belief with many leading mobile phone  network carriers and soon, HTC became the largest 
 selling brand of mobile phones at T-Mobile, AT&T, Sprint, and Verizon Wireless.
 <br><br>
Over the years, HTC also built a strong relationship with Microsoft over the years, which later on proved to be quite helpful. 
Even though, the company mainly builds smart phones using the Android OS, it also came out with the smart phones based on Windows
 7 and 8 operating systems before Nokia.


</p>
