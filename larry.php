<h6 class="bio-header">Who Is Larry Ellison?</h6>
<p class="bio-para"> 
Larry Ellison is the founder and CEO of Oracle Corporation, which earned him a spot as fifth wealthiest person in the world in 2014.
Larry Ellison was born in the Bronx, New York, on August 17, 1944, to single mother Florence Spellman. 
When he was nine months old, Ellison came down with pneumonia, and his mother sent him to Chicago to be raised by her aunt and uncle,
 Lillian and Louis Ellison, who adopted the baby.
</p>
  <h6 class="bio-header">Early life and Education</h6>
    <p class="bio-para">  
    Larry Ellison was born in New York City, to an unwed Jewish mother. His biological father was an Italian American United States 
    Army Air Corps pilot. After Ellison contracted pneumonia at the age of nine months, his mother gave him to her aunt and 
    uncle for adoption.He did not meet his biological mother again until he was 48.
    <br> <br>

Ellison moved to Chicago's South Shore, then a middle-class neighborhood. He remembers his adoptive mother as warm and loving, 
in contrast to his austere, unsupportive, and often distant adoptive father, who had adopted the name Ellison to honor his point of
 entry into the United States, Ellis Island. Louis Ellison was a government employee who had made a small fortune in Chicago real estate,
  only to lose it during the Great Depression.
  <br> <br>
Although Ellison was raised in a Reform Jewish home by his adoptive parents, who attended synagogue regularly, 
he remained a religious skeptic. Ellison states: "While I think I am religious in one sense, the particular dogmas of Judaism are not 
dogmas I subscribe to. I don't believe that they are real. They're interesting stories. They're interesting mythology, 
and I certainly respect people who believe these are literally true, but I don't. I see no evidence for this stuff." At age thirteen,
 Ellison refused to have a bar mitzvah celebration. Ellison says that his fondness for Israel is not connected to religious sentiments,
  but rather due to the innovative spirit of Israelis in the technology sector.
  <br> <br>
Ellison attended South Shore High School in Chicago and later was admitted to University of Illinois at Urbana–Champaign and was
 enrolled as a premed student. At Illinois, he was named science student of the year but later withdrew without taking final
  exams after his sophomore year, because his adoptive mother had just died. After spending the summer of 1966 in California, 
  he then attended the University of Chicago for one term, studying physics and math. He did not give any exams and at Chicago
   he first encountered computer design. In 1966, aged 22, he moved to Berkeley, California.

</p>
 
  <h6 class="bio-header">Oracle career</h6>
  <p class="bio-para"> 
 Larry Ellison lecturing at the Oracle OpenWorld, San Francisco 2010
While working at Ampex in the early 1970s, he became influenced by Edgar F. Codd's research on relational database design, 
which led in 1977 to the formation of what became Oracle. Oracle became a successful database vendor to mid- and low-range systems,
 later competing with Sybase (created 1984) and Microsoft SQL Server (a port of Sybase created in 1989) which led to Ellison being 
 listed by Forbes as one of the richest men in the world.


 
