<h6 class="bio-header">Who is Chris evans??</h6>
<p class="bio-para">Born and raised in the Boston area, Chris Evans landed his first major film role in the spoof Not Another Teen Movie.
 He played the Human Torch in two Fantastic Four flicks, but it was his turn as another superhero that propelled him to fame in the 
 enormously successful Captain America and The Avengers blockbusters. Evans also earned acclaim for his performance in the dystopian
  Snowpiercer, and made a well-received Broadway debut in 2018.  </p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">Christopher Robert Evans was born on June 13, 1981, in Boston, Massachusetts, and raised in the nearby 
    town of Sudbury. While his dad, Robert, provided financial stability as a dentist, Evans and his three siblings were drawn to 
    the performing arts world of their mother, Lisa, a dancer turned youth theater director. As Evans recalled, "We were like the von
     Trapps [from The Sound of Music], all singing and dancing."
A natural athlete, Evans wrestled and played lacrosse at Lincoln-Sudbury Regional High School, when not invested in his school
 or community theater. He spent the summer before his senior year of high school interning for a casting company in New York City, 
 making valuable contacts and honing his skills at the Lee Strasberg Institute.</p>
  <h6 class="bio-header">Personal life</h6>
  <p class="bio-para">Evans isn't the only member of his family who's made a name for himself: His younger brother Scott also 
  became an actor, holding down a regular role on the soap opera One Life to Live for two years, while an uncle, Mike Capuano, is
   a former mayor and longtime Massachusetts congressman.
The Hollywood star has been open about his battles with social anxiety, particularly when it comes to the publicity aspect of his '
profession. As he told Rolling Stone, "You're in the SUV with your family, your people. And then you have to pull over in some weird 
parking lot and do the swap. There’s security and all these people. All of a sudden you’re out of your comfort zone. 
It's the little things that can tip you over."</p>
  <h6 class="bio-header">Education</h6>
  <p class="bio-para"> Evans graduated from Lincoln-Sudbury Regional High School. He later went to New York City and took classes at 
  the Lee Strasberg Theatre and Film Institute.</p>
  <h6 class="bio-header">Relationships and affairs</h6>
<p class="bio-para">Evans was involved in an on-and-off relationship with actress Jenny Slate after they got to know one another on 
the set of The Gifted. The couple spent the 2017 holiday season together, though they reportedly split for good a few months later.
Earlier in his career, from 2001 to 2006, Evans was in a long-term relationship with Jessica Biel. He has also been linked to actresses
 Minka Kelly and Lily Collins.</p>
