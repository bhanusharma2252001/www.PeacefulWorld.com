<h6 class="bio-header">Who is Emma Watson?</h6>
<p class="bio-para">Emma Watson was born on April 15, 1990, in Paris, France, but raised in England. The actress got her big 
break as a child with Harry Potter and the Sorcerer's Stone, growing up on screen as she reprised her role of 
Hermione Granger through the franchise's eight films. Watson went on to enjoy success in the fashion and modeling
 industries, and continued to prove her abilities as an actress with roles in films like My Week with Marilyn,
  The Perks of Being a Wallflower, Beauty and the Beast and Little Women.</p>
