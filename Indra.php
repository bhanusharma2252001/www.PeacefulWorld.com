<h6 class="bio-header">Who Is Indra Nooyi?</h6>
<p class="bio-para"> 
Indra Nooyi, (born October 28, 1955, Madras [now Chennai], India), Indian-born American businesswoman who was instrumental 
in the lucrative restructuring and diversification of soft-drink manufacturer PepsiCo, Inc.’s brands. Nooyi served as the 
company’s CEO (2006–18) and chairman of the board (2007–19).

</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Barra was born in Royal Oak, Michigan. Barra's parents are of Finnish descent. Barra's ancestors are from the village of Kankaanpää, 
    in Satakunta province in Finland, from the former municipality of Köyliö. Her grandfather, Viktor Mäkelä, moved to the 
    United States and married Maria Luoma, a Finnish immigrant from Teuva. They lived in Minnesota, in the small mining town 
    of Mountain Iron. They had three children, including a son named Reino, also called Ray. Her father Ray married a second-generation 
    Finnish American named Eva Pyykkönen, and Mary was born in 1961. Ray worked for 39 years at the Pontiac car factory in Detroit, 
    and the family lived in Waterford, Michigan. Barra attended Waterford schools in Michigan. She is a graduate of Waterford Mott 
    High School.
    <h6 class="bio-header">Education</h6>
    <p class="bio-para">
    Nooyi earned a bachelor’s degree in chemistry from Madras Christian College in 1976 and a master’s degree in business 
    administration from the Indian Institute of Management in Calcutta two years later. After moving to the United States, she received an additional master’s degree in public and private management from the Yale School of Management in 1980. For the next six years, Nooyi worked as a consultant for the Boston Consulting Group. She later held executive positions at Motorola, Inc., and the engineering firm Asea Brown Boveri (now ABB).
    </p>

</p>
 
  <h6 class="bio-header">Career</h6> 
  <p class="bio-para">  
  In 1994 Nooyi joined PepsiCo as senior vice president of corporate strategy and development.
   In 2001 she was named president and chief financial officer of the company. Nooyi was responsible for guiding a
    major restructuring, which included the company’s spin-off of its restaurants—which included KFC, Pizza Hut, and 
    Taco Bell—into Tricon Global Restaurants (which later became Yum! Brands, Inc.), as well as the spin-off of PepsiCo’s 
    bottling operations. In addition, she oversaw the acquisition (1998) of Tropicana Products and a merger in 2001 with the
     Quaker Oats Co. as part of the company’s strategy of diversifying into more healthful drinks and foods.

</p>


