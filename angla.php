<h6 class="bio-header">Who Is Angela Merkel?</h6>
<p class="bio-para">Angela Merkel is a German politician best known as the first female chancellor of Germany and one of the architects
 of the European Union.

Angela Dorothea Kasner, better known as Angela Merkel, was born in Hamburg, West Germany, on July 17, 1954. Trained as a physicist,
 Merkel entered politics after the 1989 fall of the Berlin Wall. Rising to the position of chairwoman of the Christian Democratic
  Union party, Merkel became Germany's first female chancellor and one of the leading figures of the European Union, following the
   2005 national elections.
</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para"> German stateswoman and chancellor Angela Merkel was born Angela Dorothea Kasner on July 17, 1954, in Hamburg,
     Germany. The daughter of a Lutheran pastor and teacher who moved his family east to pursue his theology studies, Merkel grew up
      in a rural area north of Berlin in the then German Democratic Republic. She studied physics at the University of Leipzig, earning
       a doctorate in 1978, and later worked as a chemist at the Central Institute for Physical Chemistry, Academy of Sciences from 
       1978 to 1990.

</p>
 
  <h6 class="bio-header">Rise in the Communist Party</h6>
  <p class="bio-para">
  After the fall of the Berlin Wall in 1989, Merkel joined the Christian Democratic Union (CDU) political party. Soon after, 
  she was appointed to Helmut Kohl's cabinet as minister for women and youth, and later she served as minister for the environment
   and nuclear safety. Following Kohl's defeat in the 1998 general election, she was named secretary-general of the CDU. In 2000, 
   Merkel was chosen party leader, but she lost the CDU candidacy for chancellor to Edmund Stoiber in 2002.
   In the 2005 election, Merkel narrowly defeated Chancellor Gerhard Schröder, winning by just three seats, and after the CDU agreed a coalition deal with the Social Democrats (SPD), she was declared Germany's first female chancellor. Merkel also became the first former citizen of the German Democratic Republic to lead the reunited Germany and the first woman to lead Germany since it became a modern nation-state in 1871. She was elected to a second term in 2009.

Merkel made headlines in October 2013 when she accused the U.S. National Security Agency of tapping her cell phone. At a summit 
of European leaders she chided the United States for this privacy breech, saying that "Spying among friends is never acceptable." 
Shortly afterward, in December 2013, she was sworn in for a third term.
</p>
<h6 class="bio-header">Fourth-Term Challenges</h6>
<p class="bio-para">Angela Merkel was reelected for a fourth term as chancellor in September 2017. However, 
although her CDU party held its majority in the Bundestag, the national parliament, the far-right Alternative for Germany (AfD) 
won 13 percent of the vote to become the third-largest group in parliament, after the CDU/CSU and SPD. It was the first time a 
far-right party had entered the Bundestag since 1961. </p>
   


