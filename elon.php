<h6 class="bio-header">Who Is Elon Musk?</h6>
<p class="bio-para"> 
South African entrepreneur Elon Musk is known for founding Tesla Motors and SpaceX, which launched a landmark commercial spacecraft in 2012.

Elon Musk is a South African-born American entrepreneur and businessman who founded X.com in 1999 (which later became PayPal), SpaceX in 2002 and Tesla Motors in 2003. Musk became a multimillionaire in his late 20s when he sold his start-up company, Zip2, to a division of Compaq Computers. 

Musk made headlines in May 2012, when SpaceX launched a rocket that would send the first commercial vehicle to the International Space Station. He bolstered his portfolio with the purchase of SolarCity in 2016, and cemented his standing as a leader of industry by taking on an advisory role in the early days of President Donald Trump's administration.







</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Musk was born on June 28, 1971, in Pretoria, South Africa. As a child, Musk was so lost in his daydreams about inventions 
    that his parents and doctors ordered a test to check his hearing.

At about the time of his parents’ divorce, when he was 10, Musk developed an interest in computers. He taught himself how to program, 
and when he was 12 he sold his first software: a game he created called Blastar.
In grade school, Musk was short, introverted and bookish. He was bullied until he was 15 and went through a growth spurt and learned
 how to defend himself with karate and wrestling.


</p>
 
  <h6 class="bio-header">Family</h6>
  <p class="bio-para"> 
  Musk’s mother, Maye Musk, is a Canadian model and the oldest woman to star in a Covergirl campaign.
   When Musk was growing up, she worked five jobs at one point to support her family.

Musk’s father, Errol Musk, is a wealthy South African engineer.

Musk spent his early childhood with his brother Kimbal and sister Tosca in South Africa. His parents divorced when he was 10.
</p>
<h6 class="bio-header">Education</h6>
  <p class="bio-para"> 
  At age 17, in 1989, Musk moved to Canada to attend Queen’s University and avoid mandatory service in the South African military.
   Musk obtained his Canadian citizenship that year, in part because he felt it would be easier to obtain American citizenship via
    that path.
In 1992, Musk left Canada to study business and physics at the University of Pennsylvania. He graduated with an undergraduate degree 
in economics and stayed for a second bachelor’s degree in physics.


After leaving Penn, Musk headed to Stanford University in California to pursue a PhD in energy physics. However, his move was 
timed perfectly with the Internet boom, and he dropped out of Stanford after just two days to become a part of it, launching his 
first company, Zip2 Corporation in 1995. Musk became a U.S. citizen in 2002.
<h6 class="bio-header">Companies</h6>
<p class="bio-para">
<br>Zip2 Corporation<br>
Musk launched his first company, Zip2 Corporation, in 1995 with his brother, Kimbal Musk. An online city guide, Zip2 was soon providing
 content for the new websites of both The New York Times and the Chicago Tribune. In 1999, a division of Compaq Computer Corporation 
 bought Zip2 for $307 million in cash and $34 million in stock options.

 <br>PayPal<br>
In 1999, Elon and Kimbal Musk used the money from their sale of Zip2 to found X.com, an online financial services/payments 
company. An X.com acquisition the following year led to the creation of PayPal as it is known today.
In October 2002, Musk earned his first billion when PayPal was acquired by eBay for $1.5 billion in stock. Before the sale, 
Musk owned 11 percent of PayPal stock.

<br>SpaceX<br>
Musk founded his third company, Space Exploration Technologies Corporation, or SpaceX, in 2002 with the intention of 
building spacecraft for commercial space travel. By 2008, SpaceX was well established, and NASA awarded the company the 
contract to handle cargo transport for the International Space Station—with plans for astronaut transport in the future—in
 a move to replace NASA’s own space shuttle missions. 

 <br>Tesla Motors<br>
Musk is the co-founder, CEO and product architect at Tesla Motors, a company formed in 2003 that is dedicated to producing affordable,
 mass-market electric cars as well as battery products and solar roofs. Musk oversees all product development, engineering and design
  of the company's products.

  <br>Roadster
Five years after its formation, in March 2008, Tesla unveiled the Roadster, a sports car capable of accelerating from 0 to 60 
mph in 3.7 seconds, as well as traveling nearly 250 miles between charges of its lithium ion battery.

With a stake in the company taken by Daimler and a strategic partnership with Toyota, Tesla Motors launched its initial public
 offering in June 2010, raising $226 million.

<br>Model S<br>
In August 2008, Tesla announced plans for its Model S, the company's first electric sedan that was reportedly meant to take on the
 BMW 5 series. In 2012, the Model S finally entered production at a starting price of $58,570. Capable of covering 265 miles between
  charges, it was honored as the 2013 Car of the Year by Motor Trend magazine.

In April 2017, Tesla announced that it surpassed General Motors to become the most valuable U.S. car maker. The news was an 
obvious boon to Tesla, which was looking to ramp up production and release its Model 3 sedan later that year.

In September 2019, using what Musk described as a "Plaid powertrain," a Model S set a speed record for four-door sedan at
 Laguna Seca Raceway in Monterey County, California.

 



