<h6 class="bio-header">Who Is  Winston Churchill?</h6>
<p class="bio-para">  Winston Churchill was a British military leader and statesman. Twice named prime minister of Great Britain, 
he helped to defeat Nazi Germany in World War II.
Who Was Winston Churchill?
Sir Winston Leonard Spencer-Churchill was a British politician, military officer and writer who served as the prime minister 
of Great Britain from 1940 to 1945 and from 1951 to 1955. After becoming prime minister in 1940, Churchill helped lead a successful
 Allied strategy with the U.S. and Soviet Union during World War II to defeat the Axis powers and craft postwar peace.

</p>
  <h6 class="bio-header">Early Life</h6>
    <p class="bio-para">Churchill was born on November 30, 1874, at Blenheim Palace in Oxfordshire, England.
From an early age, young Churchill displayed the traits of his father, Lord Randolph Churchill, a British statesman from an 
established English family, and his mother, Jeanette "Jennie" Jerome, an independent-minded New York socialite.
Churchill grew up in Dublin, Ireland, where his father was employed by his grandfather, the 7th Duke of Marlborough, 
John Spencer-Churchill.
Churchill proved to be an independent and rebellious student; after performing poorly at his first two schools, Churchill in April 
1888 began attending Harrow School, a boarding school near London. Within weeks of his enrollment, he joined the Harrow Rifle Corps, 
putting him on a path to a military career.
</p>
 
  <h6 class="bio-header">Military Career</h6>
  <p class="bio-para"> 
  Churchill enjoyed a brief but eventful career in the British Army at a zenith of British military power. He joined the Fourth Queen's
   Own Hussars in 1895 and served in the Indian northwest frontier and the Sudan, where he saw action in the Battle of Omdurman in 1898.
While in the Army, he wrote military reports for the Pioneer Mail and the Daily Telegraph, and two books on his experiences,
 The Story of the Malakand Field Force (1898) and The River War (1899).
In 1899, Churchill left the Army and worked as a war correspondent for the Morning Post, a conservative daily newspaper.
 While reporting on the Boer War in South Africa, he was taken prisoner by the Boers during a scouting expedition.
He made headlines when he escaped, traveling almost 300 miles to Portuguese territory in Mozambique. Upon his return to Britain, 
he wrote about his experiences in the book London to Ladysmith via Pretoria (1900).





</p>
<h6 class="bio-header">Parliament and Cabinet</h6>
   <p class="bio-para">
In 1900, Churchill became a member of the British Parliament in the Conservative Party for Oldham, a town in Manchester. Following
 his father into politics, he also followed his father's sense of independence, becoming a supporter of social reform.
Unconvinced that the Conservative Party was committed to social justice, Churchill switched to the Liberal Party in 1904. 
He was elected a member of Parliament in 1908 and was appointed to the prime minister's cabinet as president of the Board of Trade.
As president of the Board of Trade, Churchill joined newly appointed Chancellor David Lloyd George in opposing the expansion of 
the British Navy. He introduced several reforms for the prison system, introduced the first minimum wage and helped set up labor 
exchanges and unemployment insurance.
Churchill also assisted in the passing of the People's Budget, which introduced taxes on the wealthy to pay for new social 
welfare programs. The budget passed in the House of Commons in 1909 and was initially defeated in the House of Lords before 
being passed in 1910.
In January 1911, Churchill showed his tougher side when he made a controversial visit to a police siege in London, with two alleged
 robbers holed up in a building.
Churchill's degree of participation is still in some dispute: Some accounts have him going to the scene only to see for himself
 what was going on; others state that he allegedly gave directions to police on how to best storm the building.
What is known is that the house caught fire during the siege and Churchill prevented the fire brigade from extinguishing the flames, 
stating that he thought it better to "let the house burn down," rather than risk lives rescuing the occupants. The bodies of the two
 robbers were later found inside the charred ruins.
</p>
