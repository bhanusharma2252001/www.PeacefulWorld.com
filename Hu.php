<h6 class="bio-header">Who Is Hu Jintao?</h6>
<p class="bio-para"> Hu Jintao, male, Han nationality, is a native of Jixi, Anhui Province. He was born in December 1942. 
Hu joined the CPC in April 1964 and entered the workforce in July 1965. He graduated from the Water Conservancy Engineering
 Department of Tsinghua University in 1964, where he specialized in the study of hub hydropower stations. As a university graduate,
  he holds the title of Engineer.

In the 1970s, Hu held responsible positions in Gansu Province and later in Tibet Autonomous Region. He was also active in the CYLC,
 and after moving to Beijing in the 1990s, he became president of the Central Party School. 

Hu was an alternate member, then member, of the 12th CPC Central Committee and a member of the 13th, 14th and 15th CPC Central
 Committees. Hu was a member of the Politburo Standing Committee of the 14th and 15th CPC Central Committees and a member of the
  Secretariat of the 14th and 15th CPC Central Committees. At the fourth plenary session of the 15th CPC Central Committee, Hu was
   made an additional vice-chairman of the CPC Central Military Commission. He was a member of the Standing Committee of the Sixth
    National Committee of the CPPCC. 
Hu was general secretary of the 16th CPC Central Committee, and a member of its Politburo Standing Committee. He was also president of 
the Central Party School of the 16th CPC Central Committee. 

In 2007, Hu became general secretary of the 17th CPC Central Committee and a member of its Politburo Standing Committee. 
He is also chairman of the CPC Central Military Commission, president of the PRC, chairman of the PRC Central Military Commission 
and Chairman of 17th Central Committee, Foreign Affairs Leading Small Group and Taiwan Affairs Leading Small Group.


</p>
  <h6 class="bio-header">Early Life</h6>
    <p class="bio-para"> Hu Jintao was born on 21 December 1942 in Taizhou, Jiangsu province. He is a direct descendant of the Ming 
    dynasty general Hu Zongxian, known for fighting Japanese pirates. His branch of the family migrated from Jixi County, Anhui to 
    Taizhou during his grandfather's generation. Though his father owned a small tea trading business in Taizhou, the family was 
    relatively poor. His mother was a teacher and died when he was 7, and he was raised by an aunt. Hu's father was denounced during 
    the Cultural Revolution, an event that (together with his relatively humble origins) apparently had a deep effect upon Hu, 
    who diligently tried to clear his father's name.

He joined the Chinese Communist Party (CCP) in April 1964 and began to work as an engineer in July 1965 after he graduated from 
the Water Conservancy Engineering Department at Tsinghua University, where he majored in the study of hub hydropower stations.

During his time at Tsinghua, he met his wife Liu Yongqing.

In 1968, Hu volunteered for his service in Gansu and worked on the construction of Liujiaxia Hydroelectric Station
 while also managing CCP affairs for the local branch of the Ministry of Water Resources and Electric Power. From 1969 to 1974, 
 he worked for Sinohydro Engineering Bureau as an engineer.
</p>
 
  <h6 class="bio-header">Early life and Career</h6>
  <p class="bio-para"> 
  In 1973, Hu was transferred to the Construction Department of Gansu as a secretary. The next year he was promoted to deputy senior 
  party secretary. In 1980, Deng Xiaoping implemented the "Four Transformations" program which aimed to produce communist leaders who
   were "more revolutionary, younger, more knowledgeable, and more specialized." In response to this nationwide search for young party
    members, Song Ping, the first secretary of CCP Gansu Committee (Gansu's governor) discovered Hu Jintao and promoted him several 
    ranks to the position of deputy head of the commission. Another protégé of Song, Wen Jiabao, also became prominent at the same
     time.

In 1982, Hu was promoted to the position of Communist Youth League Gansu Branch Secretary and was appointed as the director of the
 All-China Youth Federation. His mentor Song Ping was transferred to Beijing as Minister of Organization of the Communist 
 Party of China, and was in charge of senior cadres' recommendation, candidacy and promotion. With the support of Hu Yaobang and
  Deng Xiaoping, Hu was assured of a bright future in the party. At Song Ping's suggestion, in 1982 central CCP authorities 
  invited Hu to Beijing to study at the Central Party School.   Soon after, he was transferred to Beijing and appointed as 
  secretariat of the Communist Youth League Central Committee ("CY Central"). Two years later Hu was promoted to First Secretary 
  of CY Central, thus its actual leader. During his term in the Youth League, Hu escorted Hu Yaobang, who was CCP General Secretary
   then, in visits around the country. Hu Yaobang, himself a veteran coming from the Youth League, could reminiscence his youth through
    Hu's company





</p>
