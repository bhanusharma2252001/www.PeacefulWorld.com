   ensure his re-election.
He could not support Roosevelt, who had made too many political enemies among New York Democrats. Roosevelt was soundly defeated in 
the primary election and learned a valuable lesson that national stature could not defeat a well-organized local political organization.
till, Roosevelt took to Washington politics and found his career thriving as he developed more personal relationships. 
At the 1920 Democratic Convention, he accepted the nomination for vice president, as James M. Cox's running mate. The pair was 
soundly defeated by Republican Warren G. Harding in the general election, but the experience gave Roosevelt national exposure.
Roosevelt repaired his relationship with New York's Democratic political machine. He appeared at the 1924 and 1928 Democratic 
National Conventions to nominate New York governor Al Smith for president, which increased his national exposure.

