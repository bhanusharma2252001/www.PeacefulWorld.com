  <h6 class="bio-header">Personal life</h6>
    <p class="bio-para">Roosevelt was born on January 30, 1882, in Hyde Park, New York. He was born into a wealthy family as the only 
    child of James Roosevelt and Sara Ann Delano Roosevelt, and a distant cousin of President Theodore Roosevelt. 
The Roosevelts had been prominent for several generations, having made their fortune in real estate and trade, and lived at 
Springwood, their estate in the Hudson River Valley of New York State.
While growing up, Roosevelt was surrounded by privilege and a sense of self-importance. 

He was educated by tutors and governesses until age 14, and the entire household revolved around him, with his mother being 
the dominant figure in his life even into adulthood. His upbringing was very unlike the common people whom he would later champion.

In 1896, Roosevelt attended Groton School for boys, a prestigious Episcopal preparatory school in Massachusetts. The experience was 
a difficult one for him, as he did not fit in with the other students. Groton men excelled in athletics and Roosevelt did not. 

He strived to please the adults and took to heart the teachings of Groton's headmaster, Endicott Peabody, who urged students to 
help the less fortunate through public service.

</p>
 
