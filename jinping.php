  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">Xi was born June 15, 1953, the son of a well-placed Chinese Communist Party leader, Xi Zhongxun, a former 
    comrade of Communist China’s founder Mao Tse-tung. Considered a “princeling” — someone destined to rise within the government 
    due to family connections — Xi’s destiny was altered when his father was purged from power in 1962.

In 1966, Mao launched the Cultural Revolution, a sociopolitical movement intended to preserve “true” Communist ideology and purge
 remnants of capitalist society. All formal education was halted, and Xi, at that time in high school, was sent down to work in a 
 remote farming village for seven years, doing manual chores and subsisting on rice gruel. It was there that Xi grew up both physically
  and mentally. Considered a weakling when he first arrived, he grew strong and compassionate and developed good relations working 
  alongside the villagers. Though the Cultural Revolution was a failure, Xi emerged with a sense of idealism and pragmatism.





