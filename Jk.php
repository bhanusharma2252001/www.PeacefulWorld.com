<h6 class="bio-header">Who Is J.K. Rowling?</h6>
<p class="bio-para"> 
J.K. Rowling is the creator of the 'Harry Potter' fantasy series, one of the most popular book and film franchises in history.

J.K. Rowling, is a British author and screenwriter best known for her seven-book Harry Potter children's book series. 
The series has sold more than 500 million copies and was adapted into a blockbuster film franchise. 

</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Rowling was born Joanne Rowling on July 31, 1965, in Yate, England. She adopted her pen name, J.K., incorporating her grandmother's
     name, Kathleen, for the latter initial (Rowling does not have a middle name).
A graduate of the University of Exeter, Rowling moved to Portugal in 1990 to teach English. There, she met and married the Portuguese
 journalist Jorge Arantes. The couple's daughter, Jessica, was born in 1993. After her marriage ended in divorce, Rowling moved to
  Edinburgh with her daughter to live near her younger sister, Di.
While struggling to support her daughter Jessica and herself on welfare, Rowling worked on her first book in the Harry Potter series.
 The idea for the book reportedly occurred to her while she was traveling on a train from Manchester to London in 1990.


    <h6 class="bio-header"> Harry Potter: A History of Magic</h6>
    <p class="bio-para">
    In 2017, Rowling announced on her website that she would publish two new books for an exhibition at the British Library that 
    celebrates the 20th anniversary of the publication of her first novel, Harry Potter and the Philosopher’s Stone.
The books, Harry Potter: A History of Magic (described as the adult version) and Harry Potter: A Journey Through a History of Magic 
(the kid-friendly version), were released on October 20th and feature manuscripts, original illustrations and an exploration of the
 Harry Potter characters and magic.
In May 2019, it was reported that Rowling would be publishing four more Harry Potter stories. However, the author cleared up the 
confusion on her website, explaining that the “bite-sized e-reads” contain no new material. The A Journey Through… e-books were 
adapted from a companion audiobook to History of Magic narrated by Natalie Dormer.


</p>
  

