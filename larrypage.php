<h6 class="bio-header">Who Is Larry Page?</h6>
<p class="bio-para"> 
Larry Page is an internet entrepreneur and computer scientist who teamed up with grad school buddy Sergey Brin to launch the 
search engine Google in 1998.
Born in Michigan in 1973, Larry Page's parents were both computer experts. Following in their footsteps, he studied computer 
engineering at Stanford University, where he met Sergey Brin. The duo developed a search engine that listed results according 
to the popularity of the pages and, with Page as CEO, Google became the world's most popular search engine after launching in 1998. 
Page and Brin later took charge of Google's new parent company, Alphabet, until stepping down from their everyday roles in late 2019.



</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Lawrence Page was born on March 26, 1973, in East Lansing, Michigan. His father, Carl Page, was a pioneer in computer science 
    and artificial intelligence, and his mother taught computer programming. After earning a Bachelor of Science degree in 
    engineering from the University of Michigan, Page decided to concentrate on computer engineering for graduate school at 
    Stanford University, where he met Brin.

</p>
 
  <h6 class="bio-header">Creating Google With Sergey Brin</h6>
  <p class="bio-para"> 
  As a research project at Stanford University, Page and Brin created a search engine that listed results according to the 
  popularity of the pages, after concluding that the most popular result would often be the most useful. They called the 
  search engine "Google" after the mathematical term "googol," which refers to the No. 1 followed by 100 zeros, to reflect 
  their mission to organize the immense amount of information available on the web.
</p>
<h6 class="bio-header">Evolving Conglomerate</h6>
  <p class="bio-para"> 
  In 2006, Google purchased the most popular website for user-submitted streaming videos, YouTube, for $1.65 billion in stock.

In September 2013, Page was ranked No. 13 on the Forbes 400 list of the richest people in America. That October, he was ranked No. 
17 on Forbes' 2013 "Most Powerful People" list. As Google's CEO, Page shared responsibility for the company's operations with Brin,
 who served as director of special projects for Google, and Eric Schmidt, the company's executive chairman.


</p>


