<h6 class="bio-header">Who Is Johnny Depp?</h6>
<p class="bio-para">Johnny Depp landed his first legitimate movie role in Nightmare on Elm Street (1984). 
He began studying acting in earnest, the lessons paying off in 1987 when he landed a role on the TV show 21 Jump Street.
 He has since become known for his willingness to take on darker roles in films such as Edward Scissorhands (1990), 
 Sleepy Hollow (1999) and Alice in Wonderland (2010), as well as his starring efforts in the big-budget Pirates of the Caribbean movies.
 
</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">Johnny Depp was born John Christopher Depp II, in Owensboro, Kentucky, on June 9, 1963, to parents John and 
    Betty Sue Depp. Depp's father worked as a civil engineer and his mother worked as a waitress and homemaker. The youngest of four 
    children, Depp was withdrawn and a self-admitted oddball. 
Depp and his family moved frequently to accommodate his father's job, finally landing in Miramar, Florida, when Depp was 7 years old. 
The family lived in a motel for nearly a year, until his father found a job. Depp hated his new home and, by the age of 12 began smoking,
 experimenting with drugs and engaging in self-harm due to the stress of family problems. 
 In 1978, when Depp was 15, his parents got divorced. As the youngest of four, it became Depp's job to go to his father's office and pick up the 
 weekly child-support money. The split caused a rift between Depp and his father.

At 16, Depp dropped out of high school and joined a garage band called the Kids. The group became successful enough to open 
for the Talking Heads and the B-52s, but they barely made ends meet. Depp lived for months in a friend's '67 Chevy Impala.
</p>
