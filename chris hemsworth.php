<h6 class="bio-header">Who is Chris Hemsworth?</h6>
<p class="bio-para"> is known for portraying Marvel comic book hero Thor in the film series of the same name, 
and for his starring roles in 'Snow White and the Huntsman' and 'Rush.'
Born on August 11, 1983, Australian heartthrob Chris Hemsworth has made quite a name for himself by swinging his hammer as Marvel 
comic book character Thor, starring in several films under that title and in related features like The Avengers. The Melbourne native
 and married dad has also earned leading roles in Snow White and the Huntsman, Rush and In the Heart of the Sea, while showing his 
 comedic chops in reboots of Vacation and Ghostbusters.</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">Chris Hemsworth was born on August 11, 1983, in Melbourne, Australia. Surfing was his No. 2 passion — which 
    his siblings also shared — but Hemsworth knew that his first love was acting. After attending Heathmont Secondary College, the 
    aspiring star snagged minor roles in television shows Down Under. Hemsworth has one older brother, Luke, and one younger brother, 
    Liam. Both his brothers are actors as well. 

Hemsworth followed in the footsteps of other famous Aussies like Heath Ledger, Isla Fisher, Simon Baker and Naomi Watts by becoming a 
regular on the soap opera Home and Away. In 2007, after three seasons of playing character Kim Hyde, he headed stateside to continue 
his entertainment career.</p>
 
