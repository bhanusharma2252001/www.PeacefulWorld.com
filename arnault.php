<h6 class="bio-header">Who Is Bernard Arnault?</h6>
<p class="bio-para"> 

Bernard Arnault, (born March 5, 1949, Roubaix, France), French businessman best known as the chairman and CEO of the French 
conglomerate LVMH Moët Hennessy Louis Vuitton SA, the largest luxury-products company in the world.
</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    Arnault graduated from the École Polytechnique in Paris with a degree in engineering. In 1971 he took control of his father’s 
    construction firm Ferret-Savinel. Eight years later he changed the company’s name to Férinel Inc. and shifted its focus to real 
    estate.
With $15 million of his own money, Arnault, together with Antoine Bernheim, a managing partner of the French bank Lazard Frères and Co.,
 raised the $80 million necessary to purchase Boussac Saint-Frères, a bankrupt textile company that owned the fashion house of Christian
  Dior. Then, in 1987, Arnault was invited to invest in LVMH by the company’s chairman, Henri Racamier. Investing through a joint 
  venture with Guinness PLC, Arnault ousted Racamier in 1990 and started to sweep a slew of fashion companies into the LVMH fold: 
  Christian Lacroix, Givenchy, and Kenzo; the leather goods companies Loewe, Céline, and Berluti; the jeweler Fred Joailler; 
  the DFS group (the world’s biggest duty-free chain); and the beauty retailer Sephora.

Arnault was known in Europe as the man who revitalized French couture in 1995 by appointing British fashion designer John Galliano
 to replace the venerable Hubert de Givenchy at the latter’s Paris fashion house. The “Pope of Fashion,” as Arnault was dubbed by 
 Women’s Wear Daily, a year later moved Galliano to Christian Dior and appointed the brash British fashion designer Alexander McQueen
  to replace him. Arnault then hired Marc Jacobs, a young American designer, to the post of creative director at Louis Vuitton, a
   maker of luxury leather goods. By the early 21st century, Arnault’s fashion foresight had revived interest in these traditional 
   fashion houses.



</p>
 
  <h6 class="bio-header">LVMH</h6>
  <p class="bio-para"> 
  In July 1988, Arnault provided $1.5 billion to form a holding company with Guinness that held 24% of LVMH's shares. 
  In response to rumors that the Louis Vuitton group was buying LVMH's stock to form a "blocking minority", Arnault spent 
  $600 million to buy 13.5% more of LVMH, making him LVMH's largest shareholder. In January 1989, he spent another $500 million 
  to gain control of a total of 43.5% of LVMH's shares and 35% of its voting rights, thus reaching the "blocking minority" that 
  he needed to stop the dismantlement of the LVMH group.
  
  <br> On 13 January 1989, he was unanimously elected chairman of the executive
     management board.
<br>
Since then, Arnault has led the company through an ambitious development plan, transforming it into one of the largest luxury groups
 in the world, alongside Swiss luxury giant Richemont and French-based Kering. In eleven years, the sales and profit rose by a factor
  of 5, and the market value of LVMH multiplied by 15. He promoted decisions towards decentralizing the group's brands. As a result
   of these measures, the brands are now viewed as independent firms with their own history.
<br>
In July 1988, Arnault acquired Céline. In 1993, LVMH acquired Berluti and Kenzo. In the same year, Arnault bought out the French 
economic newspaper La Tribune. The company never achieved the desired success, despite his 150 million euro investment, and he 
sold it in November 2007 in order to buy a different French economic newspaper, Les Échos, for 240 million euros.
<br>
In 1994, LVMH acquired the perfume firm Guerlain. In 1996, Arnault bought out Loewe, followed by Marc Jacobs and Sephora in 1997. These
 brands were also integrated into the group: Thomas Pink in 1999, Emilio Pucci in 2000 and Fendi, DKNY and La Samaritaine in 2001.
<br>
In the 1990s, Arnault decided to develop a centre in New York to manage LVMH's presence in the United States. He chose Christian 
de Portzamparc to supervise this project. The result was the LVMH Tower that opened in December 1999.
</p>
