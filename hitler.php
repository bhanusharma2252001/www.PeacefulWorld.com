  <p class="bio-para"> In August 1914, at the outbreak of World War I, Hitler was living in Munich and voluntarily enlisted in the 
  Bavarian Army.According to a 1924 report by the Bavarian authorities, allowing Hitler to serve was almost certainly an 
  administrative error, since as an Austrian citizen, he should have been returned to Austria.[64] Posted to the Bavarian Reserve
   Infantry Regiment 16 (1st Company of the List Regiment), he served as a dispatch runner on the Western Front in France a
   nd Belgium,spending nearly half his time at the regimental headquarters in Fournes-en-Weppes, well behind the front lines.
   He was present at the First Battle of Ypres, the Battle of the Somme, the Battle of Arras, and the Battle of Passchendaele, and 
   was wounded at the Somme. He was decorated for bravery, receiving the Iron Cross, Second Class, in 1914. On a recommendation by 
   Lieutenant Hugo Gutmann, Hitler's Jewish superior, he received the Iron Cross, First Class on 4 August 1918, a decoration rarely
    awarded to one of Hitler's Gefreiter rank.He received the Black Wound Badge on 18 May 1918.

During his service at headquarters, Hitler pursued his artwork, drawing cartoons and instructions for an army newspaper. 
During the Battle of the Somme in October 1916, he was wounded in the left thigh when a shell exploded in the dispatch runners' 
dugout.Hitler spent almost two months in hospital at Beelitz, returning to his regiment on 5 March 1917. 
On 15 October 1918, he was temporarily blinded in a mustard gas attack and was hospitalised in Pasewalk. While there, Hitler 
learned of Germany's defeat, and—by his own account—upon receiving this news, he suffered a second bout of blindness.




