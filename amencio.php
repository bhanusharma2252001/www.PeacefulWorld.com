<h6 class="bio-header">Who Is Amancio Ortega?</h6>
<p class="bio-para"> 
Amancio Ortega, in full Amancio Ortega Gaona, (born March 28, 1936, Busdongo de Arbas, Spain), Spanish fashion executive 
    and founding chairman (1985) of the Spanish clothing merchandiser Inditex (Industria de Diseño Textil, SA), which included 
    the Zara chain store.
As a youth in A Coruña, in northwestern Spain, Ortega gained an entry into the garment business by working as a delivery 
boy for a men’s shirt store and as an assistant in a tailor’s shop—jobs that exposed him to the costs of manufacturing and 
delivering clothing directly to customers. He later managed a clothing store that, like the men’s shirt store, catered to a
 wealthy clientele. Ortega saw an opportunity to expand his client base by using less-expensive materials and more-efficient 
 manufacturing systems and by competitively pricing garments. He first applied the approach to a bathrobe business, Confecciones Goa,
  which he founded in 1963.
</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">  
    

Ortega founded the first Zara ready-to-wear clothing store in A Coruña in 1975, and it became not only an internationally 
famous chain but also the flagship of holding company Inditex, which he founded 10 years later. He remained the majority owner
 of the holding company, which in 2008 included the brands Stradivarius, Pull and Bear, Uterqüe, Massimo Dutti, and Oysho, in 
 addition to Zara. The operations of all Inditex businesses were based on the so-called fast-fashion concept: at fashion shows
  trend spotters picked up design ideas, in-house designers copied the best concepts, and Inditex’s highly efficient manufacturing
   operations, most of which were based in Spain, produced and delivered new fashions to stores just a few weeks after they had been
    spotted on fashion runways. In an era in which most clothing manufacturers outsourced production to China and other low-cost 
    locations, Inditex produced two-thirds of its garments in Spain and surrounding countries.



</p>
 
  <h6 class="bio-header">career</h6>
  <p class="bio-para"> 
  In 1972, he founded Confecciones Goa to sell quilted bathrobes.
In 1975, he opened his first Zara store with his wife Rosalía Mera.
<br><br>
In 2009, Zara was part of the Inditex group (Industrias de Diseño Textil Sociedad Anónima), of which Ortega owned 59.29%, and 
aside from over 6,000 stores included the brands Zara, Massimo Dutti, Oysho, Zara Home, Kiddy's Class, Tempe, Stradivarius, Pull 
and Bear, Bershka and has more than 92,000 employees.
<br><br>
His public appearance in 2000, as part of the warm-up prior to his company's initial public offering on the stock market in 2001,
 made headlines in the Spanish financial press. However, he has only ever granted interviews to three journalists.
In 2011, Ortega announced his imminent retirement from Inditex, parent company of the Zara chain, stating that he would ask Inditex 
vice-president and CEO Pablo Isla to take his place as head. In 2012 Ortega donated about €20 million to Caritas Internationalis, 
a Roman Catholic relief organisation.
<br><br>
He purchased the Torre Picasso skyscraper in Madrid. He also purchased the Epic Residences and Hotel in Miami, Florida.
<br><br>
As of 2016, he owned around 60% of Inditex, which is the holding company for Zara and related chains.
In July 2017, for its second edition of the AEF awards, the Spanish Association of Foundations awarded Amancio Ortega in the 
2017 Philanthropic Initiative category. He also donated 300 million euros to fight cancer across Spain, which were invested in the 
purchase of 440 machines to detect the disease. As a result of this, the number of Spanish public hospitals equipped with 
stereotactic radiotherapy machines has risen from 20 to 70. However, these decisions were not unanimously welcomed and were
 criticized by some political parties like Podemos.Recently, news indicate that he has bought the Troy Block complex, known to 
 the public as one of the buildings where Amazon Seattle has its headquarters.


 
