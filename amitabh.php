<p class="bio-para">Bollywood actor Amitabh Bachchan is known for his roles in action movies like 'Zanjeer,' and for hosting 

the Indian version of 'Who Wants to Be a Millionaire?'
Amitabh Bachchan is a Bollywood actor who, in 1969, debuted in Saat Hindustani. His role in 1972's Zanjeer made him an action movie star.
 In the 1980s, Bachchan held a seat in the Indian Parliament. In the 1990s, he started his own production company. He returned to 
 acting in 1997, with Mrityudaata. In 2000, he began hosting the Indian version of Who Wants to Be a Millionaire?.</p>
  <h6 class="bio-header">Early life and career</h6>
    <p class="bio-para">Amitabh Harivansh Bachchan, best known as Amitabh Bachchan, was born in Allahabad, India on October 11, 1942.
     India was still a British colony at the time, and would not achieve independence until five years later. Bachchan's father was
      the renowned Hindi poet Dr. Harivansh Rai. His mother, Teji Bachchan, was a Sikh socialite. He has one younger brother, named 
      Ajitabh.

Bachchan went to Sherwood College boarding school before enrolling in Delhi University, where he earned his Bachelor of Arts degree.
 Once he graduated, he became a freight broker in Calcutta. After a few years in Calcutta, Bachchan was ready for a change. 
 He decided to move to Bombay and take a stab at Bollywood show business. By this time, India had been independent for nearly two 
 decades, and Hindi cinema was thriving.

Early Film Career
In 1969, Bachchan made his film debut in Saat Hindustani. Although the movie tanked at the box office, Bachchan still managed to
 capture the attention of directors. Soon enough, the offers started rolling in.

By the early 1970s, Bachchan had garnered popularity with audiences as the "angry young man" in a series of successful Hindi feature
 films. His starring role in Zanjeer was particularly instrumental in launching him to stardom as an action-movie hero. Bachchan's 
 performances in films like Laawaris, Coolie, Naseeb, Silsila, Shaarabi and Jaadugar continued to enamor fans of the tall and handsome
  action hero, and also landed him multiple Fanfare Awards. From the 1970s through the early 1980s, the swashbuckling Bachchan appeared
   in more than 100 films. He seized opportunities to work with India's most acclaimed directors, such as Prakash Mehra, and dominated
    the silver screen with films like Trishul, Sholay and Chashme Buddoor. In addition to acting, Bachchan's roles often required him
     to sing.</p>
  <h6 class="bio-header">Politics and Ventures</h6>
  <p class="bio-para">In 1982, Bachchan had a serious accident while filming. Fans prayed for his recovery. 
  Bachchan survived the accident, but it prompted him to change career paths. In 1984, he traded his Bollywood stardom for a seat 
  in the Indian Parliament. His political aspirations proved to be short-lived; in 1987, he left his seat due to unexpected controversy.
  </p>
