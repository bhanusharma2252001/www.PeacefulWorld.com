<h6 class="bio-header">Who is Jackie chan?</h6>
<p class="bio-para">Martial artist and actor Jackie Chan's unique blend of impressive martial arts and screwball physical 
comedy has helped make him an international film star.
Jackie Chan began studying martial arts, drama, acrobatics and singing at age seven. Once considered a likely successor to Bruce 
Lee in Hong Kong cinema, Chan instead developed his own style of martial arts blended with screwball physical comedy. He became a 
huge star throughout Asia and went on to have hits in the United States as well.</p>
  <h6 class="bio-header">Early life and education</h6>
    <p class="bio-para">Actor, martial artist and producer Jackie Chan was born Chan Kong-sang on April 7, 1954, in Hong Kong, China.
     When his parents moved to Australia to find new jobs, the 7-year-old Chan was left behind to study at the Chinese Opera Research 
     Institute, a Hong Kong boarding school. For the next 10 years, Chan studied martial arts, drama, acrobatics and singing, and was 
     subjected to stringent discipline, including corporal punishment for poor performance. He appeared in his first film, the Cantonese 
     feature Big and Little Wong Tin Bar (1962), when he was only eight, and went on to appear in a number of musical films.
Upon his graduation in 1971, Chan found work as an acrobat and a movie stuntman, most notably in Fist of Fury (1972), starring Hong 
Kong's resident big-screen superstar, Bruce Lee. For that film, he reportedly completed the highest fall in the history of the Chinese
 film industry, earning the respectful notice of the formidable Lee, among others.</p>
 
  <h6 class="bio-header">Stunts and screen persona</h6>
  <p class="bio-para">Chan has performed most of his own stunts throughout his film career, which are choreographed by the Jackie Chan
   Stunt Team. He has stated in interviews that the primary inspiration for his more comedic stunts were films such as The General, 
   directed by and starring Buster Keaton who was also known to perform his own stunts. The team was established in 1983, and Chan has
    used them in all his subsequent films to make choreographing easier, given his understanding of each member's abilities.
    Chan and his team undertake many of the stunts performed by other characters in his films, shooting the scenes so that their 
    faces are obscured.</p>
