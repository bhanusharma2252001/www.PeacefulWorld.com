    <h6 class="bio-header">Early life and career:-</h6>

    <p class="bio-para">The only child of a literary agent and a casting director, Daniel Radcliffe achieved international 
    stardom for his work in the Harry Potter films, adaptations of the beloved best-sellers by J. K. Rowling.Radcliffe made 
    his acting debut in the 1999 television movie David Copperfield, playing the young version of the title character. He soon 
    landed a role on the big screen in 2001's spy thriller The Tailor of Panama, with Pierce Brosnan and Jamie Lee Curtis.
But even before this film's release, Radcliffe made headlines when he was cast as the lead in Harry Potter and the Sorcerer's Stone. 
The 11-year-old actor joined fellow unknown performers Rupert Grint and Emma Watson as Harry's friends, Ron Weasley and Hermione Granger, in one of the most highly anticipated movies of 2001.
  </p>

