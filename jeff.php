Who Is Jeff Bezos? 
Entrepreneur and e-commerce pioneer Jeff Bezos is the founder and CEO of the e-commerce company Amazon, owner of The
 Washington Post and founder of the space exploration company Blue Origin. His successful business ventures have made him one
  of the richest people in the world. 

Born in 1964 in New Mexico, Bezos had an early love of computers and studied computer science and electrical engineering at 
Princeton University. After graduation, he worked on Wall Street, and in 1990 he became the youngest senior vice president at
 the investment firm D.E. Shaw. 

Four years later, Bezos quit his lucrative job to open Amazon.com, an online bookstore that became one of the Internet's biggest 
success stories. In 2013, Bezos purchased The Washington Post, and in 2017 Amazon acquired Whole Foods. 




</p>
