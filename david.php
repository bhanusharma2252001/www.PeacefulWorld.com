<h6 class="bio-header">Who is David warner?</h6>
<p class="bio-para">David Andrew Warner (born 27 October 1986) is an Australian international cricketer and a former captain 
of the Australian national team. A left-handed opening batsman, Warner is the first Australian cricketer in 132 years to be 
selected for a national team in any format without experience in first-class cricket. He plays for New South Wales and the Sydney 
Thunder in domestic cricket. He served as the Australian vice-captain across Test and ODI formats of the game between 2015 and 2018.</p>
  <h6 class="bio-header">Early life</h6>
    <p class="bio-para">David Warner was born on 27 October 1986 in Paddington, a suburb in eastern Sydney.At the age of 13 he was
     asked by his coach to switch to right-handed batting because he kept hitting the ball in the air. However his mother,
      Sheila Warner (née Orange), encouraged him to return to batting left-handed and he broke the under-16's run-scoring record 
      for the Sydney Coastal Cricket Club. He then made his first grade debut for the Eastern Suburbs club at the age of 15 and later 
      toured Sri Lanka with the Australian under-19s and earned a rookie contract with the state team.
      Warner attended Matraville Public School and Randwick Boys High School.
      </p>
 
  <h6 class="bio-header">Domestic Career</h6>
  <p class="bio-para">Warner made his first-class debut for New South Wales in the final match of the 2008-09 Sheffield Shield
   competition. Warner played for Sydney Sixers in the 2012-2013 season of the Big Bash League.





</p>
