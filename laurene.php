<h6 class="bio-header">Who Is Laurene Powell Jobs?</h6>
<p class="bio-para"> 
Laurene Powell Jobs (born November 6, 1963) is an American businesswoman, executive and the founder of Emerson Collective, 
an organization that, among other investing and philanthropic activities, advocates for policies concerning education reform, 
social redistribution and environmental conservation. She is also co-founder and president of the board of College Track, which 
prepares disadvantaged high school students for college. Powell Jobs resides in Palo Alto, California, with her three children. She
 is the widow and heir of Steve Jobs, co-founder and former CEO of Apple Inc. She manages the Laurene Powell Jobs Trust
</p>
